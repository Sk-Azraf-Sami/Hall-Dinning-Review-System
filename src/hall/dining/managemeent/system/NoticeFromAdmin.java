    private void btnPostActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPostActionPerformed
           
        AdminNotice adminNotice = new AdminNotice();
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy/MM/dd HH:mm:ss");
        LocalDateTime now = LocalDateTime.now();
        String currentDateTime = dtf.format(now);

        adminNotice.setDate(currentDateTime);
        adminNotice.setNotice(txtNotice.getText());

        //ManagerNoticeDao.saveNotice(managerNotice);
        AdminNoticeDao.updateNotice(adminNotice.getDate(), adminNotice.getNotice());

        //clear();
        setVisible(false);
        new NoticeFromAdmin().setVisible(true);
    }//GEN-LAST:event_btnPostActionPerformed

