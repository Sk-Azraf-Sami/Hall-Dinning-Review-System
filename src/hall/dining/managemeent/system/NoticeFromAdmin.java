    public void showNotice()
    {
        AdminNotice adminNotice = null;
        //ArrayList<ManagerNotice> result = new ArrayList<>();
        
        try
        {
            ResultSet rs = DbOperations.getData("select *from adminNotice");
            while(rs.next())
            {
                adminNotice = new AdminNotice();
                adminNotice.setDate(rs.getString("date"));
                adminNotice.setNotice(rs.getString("notice"));
                //result.add(managerNotice);
                dateTimeLabel.setText(adminNotice.getDate());
                txtNotice.setText(adminNotice.getNotice()); 
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
        
    }
