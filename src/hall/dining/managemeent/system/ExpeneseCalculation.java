    public void validateFields() {
        String hallClose = txtHallClose.getText();
        String mealOff = txtMealOff.getText();
        String seatRent = txtSeatRent.getText();
        String admission = txtAdmission.getText();
        String establishment = txtEstablishment.getText();
        String commonRoom = txtCommonRoom.getText();
        String literature = txtLiterature.getText();
        String hallUnion = txtHallUnion.getText();
        String hallLibrary = txtHallLibrary.getText();
        String tv = txtTV.getText();
        String genaretor = txtGenaretor.getText();
        String waterSupply = txtWaterSupply.getText();
        String religion = txtReligion.getText();

        if (hallClose.matches(numPattern) && mealOff.matches(numPattern) && admission.matches(numPattern) && establishment.matches(numPattern) && commonRoom.matches(numPattern)
                && literature.matches(numPattern) && hallUnion.matches(numPattern) && hallLibrary.matches(numPattern) && tv.matches(numPattern) && genaretor.matches(numPattern)
                && waterSupply.matches(numPattern) && religion.matches(numPattern) && seatRent.matches(numPattern)) {

            if (!hallClose.equals("") && !mealOff.equals("") && !admission.equals("") && !establishment.equals("") && !commonRoom.equals("")
                    && !literature.equals("") && !hallUnion.equals("") && !hallLibrary.equals("") && !tv.equals("") && !genaretor.equals("")
                    && !waterSupply.equals("") && !religion.equals("") && !seatRent.equals("")) {
                
                btnCalculate.setEnabled(true);
            }
        } 
        
        else {
            btnCalculate.setEnabled(false);
        }
    }

