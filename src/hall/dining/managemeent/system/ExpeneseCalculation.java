    private void btnCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculateActionPerformed

        int hallClose = Integer.parseInt(txtHallClose.getText());
        int mealOff = Integer.parseInt(txtMealOff.getText());
        int admission = Integer.parseInt(txtAdmission.getText());
        int establishment = Integer.parseInt(txtEstablishment.getText());
        int commonRoom = Integer.parseInt(txtCommonRoom.getText());
        int literature = Integer.parseInt(txtLiterature.getText());
        int hallUnion = Integer.parseInt(txtHallUnion.getText());
        int hallLibrary = Integer.parseInt(txtHallLibrary.getText());
        int tv = Integer.parseInt(txtTV.getText());
        int genaretor = Integer.parseInt(txtGenaretor.getText());
        int waterSupply = Integer.parseInt(txtWaterSupply.getText());
        int religion = Integer.parseInt(txtReligion.getText());
        int seatRent = Integer.parseInt(txtSeatRent.getText());

        String month = txtMonth.getSelectedItem().toString();
        int intMonth = 0;

        if (month.equals("January") || month.equals("March") || month.equals("May") || month.equals("July")
                || month.equals("August") || month.equals("October") || month.equals("December")) {
            intMonth = 31;
        } else if (month.equals("February - 28")) {
            intMonth = 28;
        } else if (month.equals("February - 29")) {
            intMonth = 29;
        } else {
            intMonth = 30;
        }

        int perDayMeal = 55;
        int monthlyFeast = 50;
        int total = admission + establishment + commonRoom + literature + hallUnion + hallLibrary + tv + genaretor + waterSupply + religion + seatRent + monthlyFeast;
        int messing = (intMonth - hallClose) * perDayMeal;
        total = total + messing;
        int refund = mealOff * 43; // 12 tk cut off per day meal off .

        ImageIcon icon = new ImageIcon("src/popupicon/money.png");

        JOptionPane.showMessageDialog(null, "Your monthly dues: " + Integer.toString(total) + " tk. \n" + "You will get rufund " + Integer.toString(refund) + " tk. on next month", "Message", JOptionPane.INFORMATION_MESSAGE, icon);


    }//GEN-LAST:event_btnCalculateActionPerformed

